/*3_2.變數宣告與賦值-最後來的是主人(10.0)*/
/*寫一個程式，宣告一個整數變數，變數名稱為a。
然後，先指定a為1，然後指定為2，再指定為3，再指定為4，再指定為5，印出變數a最後的值。
範例如下：
a=1
a=2
a=3
a=4
a=5

輸入範例:

輸出範例:
5 */

#include <bits/stdc++.h>
#include <stdio.h>
int main() {
    int a;
    a=1;
    a=2;
    a=3;
    a=4;
    a=5;
    printf("%d\n",a);
    return 0;
}